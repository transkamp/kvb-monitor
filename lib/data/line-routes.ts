export function getLineRoute(lineNumber: string): string[] {
  const route = KVB_LINE_ROUTES.find((r) => r.line === lineNumber);
  return route ? route.stops : [];
}

export function findRouteForStop(stopName: string): { line: string; index: number } | null {
  const lowerStop = stopName.toLowerCase();
  
  for (const route of KVB_LINE_ROUTES) {
    for (let i = 0; i < route.stops.length; i++) {
      const routeStop = route.stops[i].toLowerCase();
      if (routeStop.includes(lowerStop) || lowerStop.includes(routeStop)) {
        return { line: route.line, index: i };
      }
    }
  }
  return null;
}